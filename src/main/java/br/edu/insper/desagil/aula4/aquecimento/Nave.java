package br.edu.insper.desagil.aula4.aquecimento;

public class Nave {
	private int x;
	private int y;

	public Nave(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
}
